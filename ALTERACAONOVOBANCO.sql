/*==============================================================*/
/* Table: MOVIMENTO                                             */
/*==============================================================*/
create table MOVIMENTO
(
   MOV_ID               int not null auto_increment,
   FP_AUTORIZACAO       numeric(15,0) not null,
   PRO_ID               int not null,
   MOV_ECF              numeric(3,0) not null,
   MOV_QUANTIDADE       numeric(3,0) not null,
   primary key (MOV_ID)
) ENGINE=MyISAM;

/*==============================================================*/
/* Table: PRODUTO                                               */
/*==============================================================*/
create table PRODUTO
(
   PRO_ID               int not null,
   PRO_DESCRICAO        varchar(80) not null,
   PRO_BARRAS           varchar(14),
   PRO_DIAS             numeric(3,0) not null default 0,
   PRO_ATIVO		    char(1) NOT NULL DEFAULT 'S',
   primary key (PRO_ID)
) ENGINE=MyISAM;

/*==============================================================*/
/* Table: VENDAS                                                */
/*==============================================================*/
create table VENDAS
(
   VEN_ID               int not null auto_increment,
   PRO_ID               int not null,
   VEN_AUTORIZACAO      varchar(19) not null,
   VEN_DATA_VENDA       datetime not null,
   VEN_CUPOM            numeric(9,0) not null,
   VEN_CAIXA            numeric(3,0) not null,
   VEN_QUANTIDADE       numeric(3,0) not null,
   VEN_CPF              varchar(11),
   VEN_NOME             varchar(60),
   primary key (VEN_ID)
) ENGINE=MyISAM;



